export function calculateScaledScore(rawScore: number, totalQuestions: number): number {
  // Normalize to the 77-question scale
  const normalized = Math.min(77, Math.round((rawScore / Math.max(1, totalQuestions)) * 77));
  return SCALED_LOOKUP[normalized] ?? 120;
}

export function getPercentile(scaledScore: number): number {
  return PERCENTILE_LOOKUP[Math.min(180, Math.max(120, scaledScore))] ?? 0;
}
